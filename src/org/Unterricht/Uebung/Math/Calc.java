package org.Unterricht.Uebung.Math;

public class Calc {

    public double calc1() {
        double result;
        result = Math.pow(2, 4) + Math.pow(4, 3) + Math.pow(5, 2);
        return result;
    }

    public double calc2() {
        double result;
        result = Math.pow(Math.E, 2.5-1.3);
        return result;
    }

    public double calc3() {
        double result;
        result = Math.sqrt(Math.pow(3, 2) + Math.pow(4, 2));
        return result;
    }

    public double calc4() {
        double result;
        result = (2 * Math.PI * 4) * (Math.PI * Math.pow(4, 2));
        return result;
    }
}
